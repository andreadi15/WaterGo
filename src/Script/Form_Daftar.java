//  Fungsi Pembuatan ID Pelanggan Baru Otomatis  
    private String create_id_pelanggan(){
        String lastId = null;
        String sql = "SELECT id_user FROM pelanggan ORDER BY id_user DESC LIMIT 1"; // ambil ID terakhir
        try (PreparedStatement ps = AP_Database.con.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {

            if (rs.next()) {
                lastId = rs.getString("id_user"); // misal "ORD005"
            }
        } 
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null, e);
        }
        
        String newId = "PLG001"; // default kalau tabel kosong

        if (lastId != null) {
            int number = Integer.parseInt(lastId.substring(3)); // ambil angka dari "ORD005" â†’ 5
            number++; // tambah 1
            newId = String.format("PLG%03d", number); // hasil: "ORD006"
        }
        return newId;
    }
    
//  Metode Proses Daftar Akun
    public void daftar(){
//        Memastikan Form Diisi Semua dan Tidak Ada Yang Kosong
        if (nama.getText().trim().isEmpty() ||
            username.getText().trim().isEmpty() ||
            new String(password.getPassword()).trim().isEmpty() ||
            alamat.getText().trim().isEmpty() ||
            no_telp.getText().trim().isEmpty()) {
            return;
        }        
        
//        Menginput Akun Baru User
        String user_id = create_id_pelanggan();
        String pass = new String(password.getPassword());
        String sql = "insert into pelanggan values(?,?,?,?,?,?,?,?)";
        try (PreparedStatement ps = AP_Database.con.prepareStatement(sql)) {
            ps.setString(1, user_id);
            ps.setString(2, nama.getText());
            ps.setString(3, username.getText());
            ps.setString(4, pass);
            ps.setString(5, no_telp.getText());
            ps.setString(6, alamat.getText());
            ps.setDate(7, Date.valueOf(LocalDate.now()));
            ps.setDate(8, Date.valueOf(LocalDate.now()));            
            ps.executeUpdate();
            
        } catch (Exception e) {
            e.printStackTrace();
            return;
        }
        
//        Menyimpan Data Login USer
        AP_Data.set_User(user_id, nama.getText(), alamat.getText(), no_telp.getText());
        
//        Mengosongkan Value Form Input 
        nama.setText("");
        username.setText("");
        password.setText("");
        alamat.setText("");
        no_telp.setText("");
        
//        Setelah Daftar Akun Akan Langsung Diarahkan Ke Menu Customer
        new PLG_Menu_Customer().setVisible(true);
        this.dispose();
    }
